package BancoDeDados;

public class ResumosBean {

    String idField, contratadoField, utilizandoField;

    public String getIdField() {
        return idField;
    }

    public void setIdField(String idField) {
        this.idField = idField;
    }

    public String getContratadoField() {
        return contratadoField;
    }

    public void setContratadoField(String contratadoField) {
        this.contratadoField = contratadoField;
    }

    public String getUtilizandoField() {
        return utilizandoField;
    }

    public void setUtilizandoField(String utilizandoField) {
        this.utilizandoField = utilizandoField;
    }
}
